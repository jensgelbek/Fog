package domain.items;

public class Seller {
    int sellerID;
    String name;

    public Seller( String name) {
        this.sellerID = sellerID;
        this.name = name;
    }

    public int getSellerID() {
        return sellerID;
    }

    public void setSellerID(int sellerID) {
        this.sellerID = sellerID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
